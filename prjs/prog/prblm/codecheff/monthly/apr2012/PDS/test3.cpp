bool isPDS(long long i){
    long long p=1;
    long long s=0;
    while(i>0){
        p*=(i%10);
        s+=(i%10);
        i/=10;
    }
    return p%s==0;
}

int main(){
    int cnt=0;
    long long pds=0;
    for(int i=1;;i++){
        if(isPDS(i)){
            pds++;
        }
        
        if(i==land_marks[cnt]){
            printf("%lld , %lld\n",land_marks[cnt],pds);
            cnt++;
        }
        if(pds>=1000000000LL){
            printf("Completed: %lld , %lld\n",land_marks[cnt],pds);
            break;
        }
    }
}
